"""
SIMPLE AGAR PHANTOM RECIPE
Materials: Agar powder, water, salt, container
"""

print("=== PULMO AI PHANTOM CREATION ===")
print("\nINGREDIENTS:")
print("- 500ml distilled water")
print("- 25g agar agar powder")
print("- 4.5g salt (table salt is fine)")
print("- Food coloring (optional)")
print("- Container (plastic, ~10cm diameter)")
print("\nSTEPS:")
print("1. Boil water to 90°C (small bubbles)")
print("2. Stir in agar powder slowly")
print("3. Add salt, stir until dissolved")
print("4. Pour into container")
print("5. Let cool 2 hours at room temp")
print("6. Refrigerate 4 hours to set")
print("\n✅ My lung phantom is now ready!")
